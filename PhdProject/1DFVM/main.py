"""
============================================================
 一維穩態熱傳導 FVM 求解器  ——  numpy 邊界條件教學版
============================================================
 控制方程式 :  d/dx ( k dT/dx ) + S = 0
 邊界條件   :  左右兩端 Dirichlet (固定溫度)
 求解流程   :  組裝 A T = b  ->  np.linalg.solve

 重點觀念 :
   * A 的「第 i 列」就是「第 i 個格子的能量平衡方程式」
   * A[i,i]   = 對角線 = 自己的係數 aP   (正)
   * A[i,i±1] = 鄰居係數 -aW / -aE        (負)
   * 邊界格子: 把該方向的鄰居係數換成 aB,
              已知邊界溫度移到右手側 b
============================================================
"""

import numpy as np


def solve_1d_cht(L=1.0, N=4, k=1.0, T_left=0.0, T_right=100.0, S=0.0):
    """回傳 (節點座標 xc, 數值解 T, 係數矩陣 A, 右手側 b)。"""

    # ---- 幾何 ----
    dx = L / N                            # 每個控制體積寬度
    xc = np.linspace(dx/2, L - dx/2, N)   # 節點中心座標 (注意是 dx/2 起算)

    # ---- 導熱係數 ----
    a    = k / dx        # 內部連結:節點間距 = dx
    aB   = k / (dx/2)    # 邊界連結:節點到邊界面距離 = dx/2 (= 2a)
    bsrc = S * dx        # 源項對 b 的貢獻

    # ---- 配置空的矩陣與向量 ----
    A = np.zeros((N, N))
    b = np.zeros(N)

    # ---- 逐格填入係數 ----
    for i in range(N):
        if i == 0:                        # === 左邊界格子 ===
            A[i, i]   = aB + a            #   對角線 aP = aB(西,邊界) + a(東)
            A[i, i+1] = -a               #   東鄰居
            b[i]      = aB * T_left + bsrc  # 已知西邊界溫度 -> b
        elif i == N - 1:                  # === 右邊界格子 ===
            A[i, i-1] = -a               #   西鄰居
            A[i, i]   = a + aB           #   對角線 aP = a(西) + aB(東,邊界)
            b[i]      = aB * T_right + bsrc # 已知東邊界溫度 -> b
        else:                             # === 內部格子 ===
            A[i, i-1] = -a               #   西鄰居
            A[i, i]   = 2 * a            #   對角線 aP = aW + aE = 2a
            A[i, i+1] = -a               #   東鄰居
            b[i]      = bsrc

    # ---- 求解線性系統 A T = b ----
    T = np.linalg.solve(A, b)
    return xc, T, A, b


